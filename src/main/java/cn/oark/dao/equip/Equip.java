package cn.oark.dao.equip;

public class Equip {
    private String eq_id = "";
    private String eq_type = "";
    private int eq_level = 0;

    public String getEq_id() {
        return eq_id;
    }

    public void setEq_id(String eq_id) {
        this.eq_id = eq_id;
    }

    public String getEq_type() {
        return eq_type;
    }

    public void setEq_type(String eq_type) {
        this.eq_type = eq_type;
    }

    public int getEq_level() {
        return eq_level;
    }

    public void setEq_level(int eq_level) {
        this.eq_level = eq_level;
    }
}
